import React, { useState, useEffect, useRef } from 'react';
import { Badge } from '@fluentui/react-components';
import {
  Dismiss24Regular,
  Brain24Regular,
  Person24Regular,
  Flash24Regular,
} from '@fluentui/react-icons';
import { useApp } from '../context/AppContext';
import type { WorkOrder, AIInsight, Crew } from '../types/infrastructure';

/* ── Animated count-up hook ── */
function useCountUp(target: number, duration = 1200, active = true): number {
  const [value, setValue] = useState(0);
  const rafRef = useRef<number>(0);
  useEffect(() => {
    if (!active) { setValue(0); return; }
    const start = performance.now();
    const tick = (now: number) => {
      const t = Math.min((now - start) / duration, 1);
      // ease-out cubic
      const ease = 1 - Math.pow(1 - t, 3);
      setValue(Math.round(ease * target));
      if (t < 1) rafRef.current = requestAnimationFrame(tick);
    };
    rafRef.current = requestAnimationFrame(tick);
    return () => cancelAnimationFrame(rafRef.current);
  }, [target, duration, active]);
  return value;
}

interface HealthGrade {
  grade: string;
  label: string;
  color: string;
}

interface BriefingOverlayProps {
  workOrders: WorkOrder[];
  insights: AIInsight[];
  crews: Crew[];
  clusterCount: number;
  getHealthGrade: () => HealthGrade;
}

const BriefingOverlay: React.FC<BriefingOverlayProps> = ({
  workOrders,
  insights,
  crews,
  clusterCount,
  getHealthGrade,
}) => {
  const { isOverlay, closeOverlay } = useApp();
  const visible = isOverlay('briefing');

  // Real targets
  const critCount = workOrders.filter(w => w.severity === 'critical').length;
  const highCount = workOrders.filter(w => w.severity === 'high').length;
  const totalCount = workOrders.length;
  const repairK = Math.round(workOrders.reduce((s, w) => s + w.estimatedCost, 0) / 1000);
  const schoolCount = workOrders.filter(w => w.nearSchool).length;
  const crewCount = crews.length;

  // Animated values
  const aCrit = useCountUp(critCount, 1000, visible);
  const aHigh = useCountUp(highCount, 1000, visible);
  const aTotal = useCountUp(totalCount, 1200, visible);
  const aRepair = useCountUp(repairK, 1400, visible);
  const aSchool = useCountUp(schoolCount, 1000, visible);
  const aCrew = useCountUp(crewCount, 800, visible);
  const aManualHrs = useCountUp(Math.round(totalCount * 0.4), 1200, visible);
  const aSpeedup = useCountUp(Math.round(totalCount * 0.4 * 3600 / 2), 1600, visible);

  // Cost / ROI targets (hooks MUST be at top level, not inside JSX)
  const aAnnualManual = useCountUp(Math.round((totalCount * 25 / 60) * 45 * 12 + crewCount * 2 * 260 * 45), 1400, visible);
  const aAnnualAI = useCountUp(Math.round(totalCount * 0.8 * 0.0012 * 30 * 12 + 50 * 12), 1400, visible);
  const aROI = useCountUp(Math.round(((totalCount * 25 / 60) * 45 * 12) / Math.max(totalCount * 0.8 * 0.0012 * 30 * 12 + 600, 1) * 100), 1600, visible);

  if (!visible) return null;

  const health = getHealthGrade();

  return (
    <div className="briefing-overlay" onClick={() => closeOverlay()}>
      <div className="briefing-card" onClick={(e) => e.stopPropagation()}>
        <div className="briefing-header">
          <div>
            <h2 className="briefing-title">Infrastructure Executive Briefing</h2>
            <p className="briefing-subtitle">Lake Forest, IL — {new Date().toLocaleDateString('en-US', { month: 'long', day: 'numeric', year: 'numeric' })}</p>
            <p className="briefing-ai-badge">Generated by MAINTAIN AI in 0.3 seconds</p>
          </div>
          <button className="briefing-close" onClick={() => closeOverlay()}>
            <Dismiss24Regular />
          </button>
        </div>

        <div className="briefing-body">
          {/* Health Grade Hero — pulsing */}
          <div className="briefing-grade-hero">
            <div className="briefing-grade-circle briefing-grade-pulse" style={{ borderColor: health.color, color: health.color, fontSize: health.grade.length > 2 ? 22 : 32 }}>
              {health.grade}
            </div>
            <div>
              <h3 style={{ margin: 0, fontSize: 18 }}>Infrastructure Score: <span style={{ color: health.color }}>{health.label}</span></h3>
              <p style={{ margin: '4px 0 0', color: 'var(--text-secondary)', fontSize: 13 }}>
                Based on analysis of {totalCount} active work orders across Lake Forest
              </p>
            </div>
          </div>

          {/* Key Numbers — count-up */}
          <div className="briefing-stats-grid">
            <div className="briefing-stat">
              <span className="briefing-stat-num" style={{ color: 'var(--priority-critical)' }}>{aCrit}</span>
              <span className="briefing-stat-label">Critical Issues</span>
            </div>
            <div className="briefing-stat">
              <span className="briefing-stat-num" style={{ color: 'var(--accent-warning)' }}>{aHigh}</span>
              <span className="briefing-stat-label">High Priority</span>
            </div>
            <div className="briefing-stat">
              <span className="briefing-stat-num" style={{ color: 'var(--accent-primary)' }}>{aTotal}</span>
              <span className="briefing-stat-label">Total Active</span>
            </div>
            <div className="briefing-stat">
              <span className="briefing-stat-num" style={{ color: 'var(--accent-success)' }}>${aRepair}K</span>
              <span className="briefing-stat-label">Repair Estimate</span>
            </div>
            <div className="briefing-stat">
              <span className="briefing-stat-num" style={{ color: 'var(--accent-secondary)' }}>{aSchool}</span>
              <span className="briefing-stat-label">Near Schools</span>
            </div>
            <div className="briefing-stat">
              <span className="briefing-stat-num" style={{ color: 'var(--accent-purple)' }}>{aCrew}</span>
              <span className="briefing-stat-label">Active Crews</span>
            </div>
          </div>

          {/* Issue Breakdown */}
          <div className="briefing-section">
            <h4 className="briefing-section-title">Issue Distribution</h4>
            {(['pothole', 'sidewalk', 'concrete'] as const).map(type => {
              const typeOrders = workOrders.filter(w => w.issueType === type);
              const pct = workOrders.length > 0 ? Math.round((typeOrders.length / workOrders.length) * 100) : 0;
              const colors: Record<string, string> = { pothole: 'var(--accent-warning)', sidewalk: 'var(--accent-primary)', concrete: 'var(--accent-purple)' };
              return (
                <div key={type} className="briefing-bar-row">
                  <span className="briefing-bar-label">{type.charAt(0).toUpperCase() + type.slice(1)}</span>
                  <div className="briefing-bar-track">
                    <div className="briefing-bar-fill" style={{ width: `${pct}%`, background: colors[type] }} />
                  </div>
                  <span className="briefing-bar-val">{typeOrders.length} ({pct}%)</span>
                </div>
              );
            })}
          </div>

          {/* AI Insights Summary */}
          {insights.length > 0 && (
            <div className="briefing-section">
              <h4 className="briefing-section-title">AI Recommendations</h4>
              {insights.slice(0, 4).map((ins, i) => (
                <div key={i} className="briefing-insight-row">
                  <span className="briefing-insight-num">{i + 1}</span>
                  <div>
                    <strong>{ins.title}</strong>
                    <p style={{ margin: '2px 0 0', fontSize: 12, color: 'var(--text-secondary)' }}>
                      {ins.recommendation.slice(0, 120)}{ins.recommendation.length > 120 ? '...' : ''}
                    </p>
                  </div>
                  <Badge color={ins.confidence >= 0.8 ? 'success' : 'warning'} size="small">
                    {Math.round(ins.confidence * 100)}%
                  </Badge>
                </div>
              ))}
            </div>
          )}

          {/* AI vs Manual Comparison — count-up */}
          <div className="briefing-section">
            <h4 className="briefing-section-title">AI vs. Traditional Analysis</h4>
            <div className="briefing-comparison">
              <div className="briefing-compare-col ai">
                <span className="briefing-compare-icon"><Brain24Regular style={{ width: 20, height: 20 }} /></span>
                <span className="briefing-compare-label">MAINTAIN AI</span>
                <span className="briefing-compare-time">~2 seconds</span>
                <span className="briefing-compare-detail">{aTotal} assets analyzed, {insights.length} insights, {clusterCount} clusters, {aCrew} crews optimized</span>
              </div>
              <div className="briefing-compare-vs">VS</div>
              <div className="briefing-compare-col manual">
                <span className="briefing-compare-icon"><Person24Regular style={{ width: 20, height: 20 }} /></span>
                <span className="briefing-compare-label">Manual GIS Process</span>
                <span className="briefing-compare-time">~{aManualHrs} hours</span>
                <span className="briefing-compare-detail">Data collection, spatial analysis, report writing, crew scheduling, route optimization</span>
              </div>
            </div>
            <div className="briefing-speedup">
              <Flash24Regular style={{ width: 14, height: 14 }} /> {aSpeedup.toLocaleString()}× faster
            </div>
          </div>

          {/* Cost / ROI Projections — Real Usage Data */}
          <div className="briefing-section">
            <h4 className="briefing-section-title">💰 Cost / ROI Projections</h4>
            <p style={{ fontSize: 12, color: 'var(--text-secondary)', margin: '0 0 10px' }}>
              Based on {aTotal} active work orders • Municipal benchmarks (APWA, GAO, FHWA)
            </p>
            <div className="briefing-stats-grid" style={{ gridTemplateColumns: 'repeat(3, 1fr)' }}>
              <div className="briefing-stat">
                <span className="briefing-stat-num" style={{ color: 'var(--accent-success)', fontSize: 22 }}>
                  ${aAnnualManual.toLocaleString()}
                </span>
                <span className="briefing-stat-label">Annual Manual Cost</span>
              </div>
              <div className="briefing-stat">
                <span className="briefing-stat-num" style={{ color: 'var(--accent-primary)', fontSize: 22 }}>
                  ${aAnnualAI.toLocaleString()}
                </span>
                <span className="briefing-stat-label">Annual AI Platform Cost</span>
              </div>
              <div className="briefing-stat">
                <span className="briefing-stat-num" style={{ color: '#10b981', fontSize: 22 }}>
                  {aROI.toLocaleString()}%
                </span>
                <span className="briefing-stat-label">Return on Investment</span>
              </div>
            </div>
            <div style={{ display: 'flex', gap: 10, marginTop: 8 }}>
              <div style={{ flex: 1, background: 'var(--glassmorphism-bg, rgba(16,185,129,0.08))', borderRadius: 8, padding: '8px 12px' }}>
                <span style={{ fontSize: 11, color: 'var(--text-secondary)', display: 'block' }}>Per Work Order</span>
                <span style={{ fontSize: 15, fontWeight: 600 }}>
                  <span style={{ color: 'var(--text-secondary)', textDecoration: 'line-through', fontSize: 12 }}>
                    ${(25 * 45 / 60).toFixed(2)}
                  </span>
                  {' → '}
                  <span style={{ color: 'var(--accent-success)' }}>
                    ${(totalCount > 0 ? (totalCount * 0.8 * 0.0012 / totalCount) : 0).toFixed(4)}
                  </span>
                </span>
              </div>
              <div style={{ flex: 1, background: 'var(--glassmorphism-bg, rgba(59,130,246,0.08))', borderRadius: 8, padding: '8px 12px' }}>
                <span style={{ fontSize: 11, color: 'var(--text-secondary)', display: 'block' }}>Crew Route Optimization</span>
                <span style={{ fontSize: 15, fontWeight: 600, color: 'var(--accent-success)' }}>30% less drive time</span>
              </div>
              <div style={{ flex: 1, background: 'var(--glassmorphism-bg, rgba(168,85,247,0.08))', borderRadius: 8, padding: '8px 12px' }}>
                <span style={{ fontSize: 11, color: 'var(--text-secondary)', display: 'block' }}>Liability Reduction</span>
                <span style={{ fontSize: 15, fontWeight: 600, color: 'var(--accent-purple)' }}>40% lower risk</span>
              </div>
            </div>
            <p style={{ fontSize: 10, color: 'var(--text-tertiary)', marginTop: 6, textAlign: 'right', fontStyle: 'italic' }}>
              Sources: APWA 2024-25 Benchmarks • GAO-23-105610 • FHWA Infrastructure Guidelines
            </p>
          </div>

          {/* Powered by Microsoft — tech stack ribbon */}
          <div className="briefing-tech-ribbon">
            <span className="briefing-tech-label">Powered by</span>
            <div className="briefing-tech-logos">
              <span className="briefing-tech-chip">Azure OpenAI</span>
              <span className="briefing-tech-chip">Semantic Kernel</span>
              <span className="briefing-tech-chip">Fluent UI</span>
              <span className="briefing-tech-chip">Power Apps</span>
              <span className="briefing-tech-chip">Dataverse</span>
              <span className="briefing-tech-chip">Content Safety</span>
            </div>
          </div>

          {/* Footer */}
          <div className="briefing-footer">
            <span>MAINTAIN AI • MCP Agent Framework • Real-time Infrastructure Intelligence</span>
          </div>
        </div>
      </div>
    </div>
  );
};

export default BriefingOverlay;
